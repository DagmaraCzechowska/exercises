package StrWww;

public class Human {
    int age;
    int weight;
    int height;
    String name;
    String male;

    public int getAge() {
        return age;
    }

    public int getWeight() {
        return weight;
    }

    public int getHeight() {
        return height;
    }

    public String getName() {
        return name;
    }

    public String isMale() {
        return male;
    }
}
